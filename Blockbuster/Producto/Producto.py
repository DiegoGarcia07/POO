class Producto:
    def __init__(self, tipo, nombre, año, género, precioDeVenta, precioDeRenta, disponible, director, temporada):
        self.tipo = tipo
        self.nombre = nombre
        self.año = año
        self.género = género
        self.precioDeVenta = precioDeVenta
        self.precioDeRenta = precioDeRenta
        self.disponible = disponible
        self.director = director
        self.temporada = temporada